    public Ranking(Lobby volver) {
        lobby = volver;
        ranking = new Modelo.Ranking(); 
        initComponents();
        setLocationRelativeTo(null); // Centra la ventana
        //No modificar el tama√±o de la ventana:
        this.setResizable(false);



        // Inicializamos el TextArea con encabezado
        StringBuilder sb = new StringBuilder();
        sb.append("Lista De Ranking\n\n");

        //-------------------------------------------------------------------------------------------
        List<Jugador> listaRanking = ranking.obtenerRanking(); // obtenemos jugadores del archivo
        //HAcemos una busqueda para mostrar los jugdores de la lsita en el ranking
        if (listaRanking != null && !listaRanking.isEmpty()) {
            for (int i = 0; i < listaRanking.size(); i++) {
                Jugador j = listaRanking.get(i);
                //elimina si hay alguno en la lista menor a 25.
                if (j.getSaldo() < 25){
                    ranking.eliminarJugadorDeRanking(j.getNombre());
                }
                // Agregamos cada jugador al StringBuilder
                sb.append((i + 1) + ". " + j.getNombre() + " - Saldo: $" + j.getSaldo() + "\n");
            }
        } else {
            sb.append("No hay jugadores en el ranking.\n");
        }
        //-------------------------------------------------------------------------------------------



        // Mostramos todo en el TextArea
        this.textArea1.setText(sb.toString());





    }

